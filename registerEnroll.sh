#!/bin/bash

function createuser() {
  echo "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/user.scholarship.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/user.scholarship.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-user --tls.certfiles "${PWD}/organizations/fabric-ca/user/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-user.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-user.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-user.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-user.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/user.scholarship.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy user's CA cert to user's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/user.scholarship.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/user/ca-cert.pem" "${PWD}/organizations/peerOrganizations/user.scholarship.com/msp/tlscacerts/ca.crt"

  # Copy user's CA cert to user's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/user.scholarship.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/user/ca-cert.pem" "${PWD}/organizations/peerOrganizations/user.scholarship.com/tlsca/tlsca.user.scholarship.com-cert.pem"

  # Copy user's CA cert to user's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/user.scholarship.com/ca"
  cp "${PWD}/organizations/fabric-ca/user/ca-cert.pem" "${PWD}/organizations/peerOrganizations/user.scholarship.com/ca/ca.user.scholarship.com-cert.pem"

  echo "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-user --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/user/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering peer1"
  set -x
  fabric-ca-client register --caname ca-user --id.name peer1 --id.secret peer1pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/user/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering user"
  set -x
  fabric-ca-client register --caname ca-user --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/user/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-user --id.name useradmin --id.secret useradminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/user/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-user -M "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer0.user.scholarship.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/user/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/user.scholarship.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer0.user.scholarship.com/msp/config.yaml"

  echo "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-user -M "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer0.user.scholarship.com/tls" --enrollment.profile tls --csr.hosts peer0.user.scholarship.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/user/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer0.user.scholarship.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer0.user.scholarship.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer0.user.scholarship.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer0.user.scholarship.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer0.user.scholarship.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer0.user.scholarship.com/tls/server.key"

  echo "Generating the peer1 msp"
  set -x
  fabric-ca-client enroll -u https://peer1:peer1pw@localhost:7054 --caname ca-user -M "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer1.user.scholarship.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/user/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/user.scholarship.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer1.user.scholarship.com/msp/config.yaml"

  echo "Generating the peer1-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer1:peer1pw@localhost:7054 --caname ca-user -M "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer1.user.scholarship.com/tls" --enrollment.profile tls --csr.hosts peer1.user.scholarship.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/user/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer1.user.scholarship.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer1.user.scholarship.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer1.user.scholarship.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer1.user.scholarship.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer1.user.scholarship.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/user.scholarship.com/peers/peer1.user.scholarship.com/tls/server.key"


  echo "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-user -M "${PWD}/organizations/peerOrganizations/user.scholarship.com/users/User1@user.scholarship.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/user/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/user.scholarship.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/user.scholarship.com/users/User1@user.scholarship.com/msp/config.yaml"

  echo "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://useradmin:useradminpw@localhost:7054 --caname ca-user -M "${PWD}/organizations/peerOrganizations/user.scholarship.com/users/Admin@user.scholarship.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/user/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/user.scholarship.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/user.scholarship.com/users/Admin@user.scholarship.com/msp/config.yaml"
}

