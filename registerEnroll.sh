  cp "${PWD}/organizations/ordererOrganizations/scholarship.com/orderers/orderer.scholarship.com/tls/tlscacerts/"* "${PWD}/organizations/ordererOrganizations/scholarship.com/orderers/orderer.scholarship.com/msp/tlscacerts/tlsca.scholarship.com-cert.pem"

